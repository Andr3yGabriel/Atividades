portas = float(input("Digite a posição das portas, sendo P o primeiro dígito e R o segundo: "))

if portas == 1.0:
    print("Caminho C")

elif portas == 0.1:
    print("Caminho B")

elif portas == 0.0:
    print("Caminho A")

elif portas == 1.1:
    print("Caminho C")

else:
    print("Comando inválido")